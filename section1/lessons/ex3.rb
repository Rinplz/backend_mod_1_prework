# > compares if the number on the left is higher in value than the one on the right
puts "Is it greater? #{5 > -2}"
# >= compares if the number on the left is higher or equal in value to the number on the right
puts "Is it greater or equal? #{5 >= -2}"
# <= compares if the number on the left is lower or equal in value to the number on the right
puts "Is it less or equal? #{5 <= -2}"
